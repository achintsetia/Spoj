#define MAX 2005

vector<int> G[MAX];
int color[MAX];

bool BFS(int start)
{
  color[start] = 1;
  queue<int> Q;

  Q.push(start);

  int u,v;
  while(!Q.empty())
  {
    u = Q.front(); Q.pop();
    int s = G[u].size();
    for( int i=0; i<s; i++)
    {
      v = G[u][i];
      if( color[v] == color[u]) return false;
      if( !color[v])
      {
        color[v] = 3 - color[u];
        Q.push(v);
      }
    }
  }
  return true;
}

int main( int argc, char** argv)
{
  int T = input.readInt();

  for( int t=1; t<=T; t++)
  {
    int N = input.readInt();
    int E = input.readInt();

    for( int i=1; i<=N; i++)
    {
      G[i].clear();
      color[i] = 0;
    }

    for( int i=0; i<E; i++)
    {
      int A = input.readInt();
      int B = input.readInt();

      G[A].push_back(B);
      G[B].push_back(A);
    }

    int i=1;
    for( i=1; i<=N; i++)
    {
      if( !color[i])
      {
        if( BFS(i)) continue;
        else break;
      }
    }
    output("Scenario #", t, ":\n");
    if( i>N) output("No suspicious bugs found!\n");
    else output("Suspicious bugs found!\n");
  }
  return 0;
}
